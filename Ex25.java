package javaproject;



public class Ex25 {
	
	public static void main(String[] args) {
		
		int fruit = 2;
		switch(fruit) {
		case 2: 
			System.out.println("apple");
			break;
		case 'B': 
			System.out.println("banana");
			break;
		case 'C': 
			System.out.println("coconut");
			break;
		default:
			System.out.println("none");
		}
	}
}