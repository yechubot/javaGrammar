package javaproject;

public class Ex16 {
	public static void main(String[] args) {
		
		for(int a=1; a<=5; a++) {	
			for(int b=1; b<=a; b++) {
				System.out.print(b);
			 
			}System.out.println();
			
		}System.out.println();
		
	
		for(int a=1; a<=5; a++) {	
			for(int b=1; b<=a; b++) {
				System.out.print(a);
			 
			}System.out.println();
			
		}System.out.println();
		
		for(int a=1; a<=5; a++) {	
			for(int b=1; b<=6-a; b++) {
				System.out.print(b);
			 
			}System.out.println();
			
		}System.out.println();
		
	}

}

